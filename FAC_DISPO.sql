CREATE OR REPLACE TABLE `crp-pro-dwh-analiticagold.EIL_DP_RAW_TMP.DIM_NEGFIN_TMP_3_DIM`
AS
SELECT DISTINCT  A.CTA_CVE, A.TIP_INF,ANIO_ESTR, MES_ESTR,CTA_FCH_ALTA,PLAN_CVE, 
CASE WHEN PLAN_CVE IN (20002) THEN 'ATM' WHEN PLAN_CVE IN (20003) THEN 'POS' WHEN PLAN_CVE IN (20004) THEN 'DIGITAL'  ELSE 'POS' END AS FLG_CANAL,


        
        SUM(CTOS_IMP) AS CANT_TRN,
       SUM(CASE WHEN A.TRN_CVE in (62, 262, 420,1062,1262 ) THEN TRN_IMP*-1             
                WHEN (A.TRN_CVE in (2,102,122,320,1002,1102,2002,2102) ) THEN TRN_IMP         
                WHEN (/*TRN_CVE  IN (789,339)*/ PLAN_CVE IN (20003,20004) and a.TRN_CVE  in (12,122) AND A.TIP_INF IN (200, 210) ) THEN TRN_IMP
                WHEN (/*TRN_CVE  IN (789,339)*/  PLAN_CVE IN (20003) and a.TRN_CVE  in (72,282) AND A.TIP_INF IN (200, 210) ) THEN TRN_IMP*-1						
          ELSE 0 END) AS IMP_TRN,
FROM (
            SELECT  CTA_CVE, A.TIP_INF, EXTRACT (YEAR FROM A.FCH_FCH) AS ANIO_ESTR,
                    EXTRACT (MONTH FROM A.FCH_FCH) AS MES_ESTR,  A.TRN_CVE,PLAN_CVE ,
                    
                    CASE WHEN (A.TRN_CVE IN (2,102,320,122) AND A.TIP_INF =110) THEN  1 
                        WHEN (A.TRN_CVE IN (2,102,320,1002,1102,2002,2102,122) AND A.TIP_INF =100) THEN 1
                            WHEN (PLAN_CVE IN (20003,20004) and a.TRN_CVE  in (12,122) AND A.TIP_INF IN (200, 210) ) THEN 1
                    ELSE 0 END AS CTOS_IMP, 
                    A.TRN_IMP,
                
            FROM (
                        SELECT   CASE WHEN (C.CTA_ACT IS NULL OR C.CTA_ACT=0) THEN A.CTA_CVE ELSE C.CTA_ACT END CTA_CVE, A.TIP_INF, FCH_FCH, TRN_CVE, TRN_IMP, TDA_CVE,PLAN_CVE--,CTA_NIV_MOR
                        FROM `crp-pro-dwh-semanticagold.EIL_DP_VDWH.VFAC_TRN_CRD` AS A 
                            LEFT JOIN `crp-pro-dwh-semanticagold.EIL_DP_VDWH.VDIM_CTA_TRACK` AS C ON A.CTA_CVE=C.CTA_CVE

                        WHERE FCH_FCH BETWEEN (SELECT FCH_INI FROM `crp-pro-dwh-analiticagold.EIL_DP_RAW_TMP.DIM_NEGFIN_TMP_TIEMPO_DISP`) AND (SELECT FCH_FIN FROM `crp-pro-dwh-analiticagold.EIL_DP_RAW_TMP.DIM_NEGFIN_TMP_TIEMPO_DISP`)
                            AND TRN_CVE IN (420,2,62,102,262,320,1002,1062,1102,1262,2002,2102,022,282,122,72,12)
                            AND TIP_INF IN (100, 110,210,200)
                            AND PLAN_CVE IN (20002,20004,20003)
                ) AS A	
        ) AS A
LEFT JOIN `crp-pro-dwh-semanticagold.EIL_DP_VDWH.VDIM_CTA` AS C ON A.CTA_CVE=C.CTA_CVE
GROUP BY 1,2,3,4,5,6;


