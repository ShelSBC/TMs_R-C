CREATE OR REPLACE TABLE `crp-pro-dwh-analiticagold.EIL_DP_RAW_TMP.DIM_NEGFIN_TMP_1_TITULAR`
AS
( 
SELECT * FROM  (
                SELECT CTA_CVE,
                SUM(CANT_ADIC) AS FLG_ADIC,
                SUM(CANT_TIT) AS FLG_TITU
                FROM (
                        SELECT A.CTA_CVE
                        ,0 AS CANT_TIT
                        ,COUNT((A.TRJ_NUM)) AS CANT_ADIC
                        FROM `crp-pro-dwh-semanticagold.EIL_DP_VDWH.VDIM_CTA_TRJ` AS A
                        WHERE IND_TIT IN ('1',' ') 
                        AND A.TRJ_EDO_CVE NOT IN ('A ','B ','C ','E ','F','H ','J ','K ','O ','P ','Q ','S ','U ','X ','Y ','Z')
                        GROUP BY 1
                UNION ALL
                        SELECT B.CTA_CVE
                        ,COUNT((B.TRJ_NUM)) AS CANT_TIT
                        ,0 AS CANT_ADIC
                        FROM `crp-pro-dwh-semanticagold.EIL_DP_VDWH.VDIM_CTA_TRJ` AS B
                        WHERE IND_TIT='0'AND 
                        B.TRJ_EDO_CVE NOT IN ('A ','B ','C ','E ','F','H ','J ','K ','O ','P ','Q ','S ','U ','X ','Y ', 'Z')
                        GROUP BY 1
                )GROUP BY 1
    ) 
);

