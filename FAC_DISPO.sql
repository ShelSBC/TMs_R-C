CREATE OR REPLACE TABLE `crp-pro-dwh-analiticagold.EIL_DP_RAW_TMP.DIM_NEGFIN_TMP_TIEMPO_DISP`
AS 



SELECT * 
FROM (
    SELECT  
    CASE WHEN (EXTRACT(DAY FROM DATE('2026-07-01')) = 1 AND FLG_RPC = FALSE) THEN DATE_TRUNC(DATE_ADD(DATE('2026-07-01'), INTERVAL -1 DAY), MONTH)
         WHEN (EXTRACT(DAY FROM DATE('2026-07-01')) = 1 AND FLG_RPC = TRUE) THEN DATE('2017-01-01')
        ELSE DATE_TRUNC(DATE('2026-07-01'), MONTH)
        END AS FCH_INI,

    CASE WHEN EXTRACT(DAY FROM DATE('2026-07-01')) = 1 
        THEN LAST_DAY(DATE_ADD(DATE('2026-07-01'), INTERVAL -1 DAY), MONTH)
        ELSE LAST_DAY(DATE('2026-07-01'), MONTH)
        END AS FCH_FIN,

    
    'TM_DISPOSICIONES' AS ESTRATEGIA
);




           
